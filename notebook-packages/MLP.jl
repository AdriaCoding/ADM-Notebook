# ╔═╡ 1fcb4488-b46a-47cf-83b8-85a97e052423
function loss_ab(x,y) 
	pred = model_ab(x)
	return mean((pred .- y).^2)
end

# ╔═╡ 6753be48-af78-42aa-9f0d-028498a3bcd3
md"**3. Optimizer**  

